flicker_em:
	lda sprite_flicker
	cmp #$01
	beq @go_backward_init

@go_forward_init:
	ldy #$00
@go_forward:
	lda r_left_side, Y
	sta OAM_ram, Y
	iny
	lda r_left_side, Y
	sta OAM_ram, Y
	iny
	lda r_left_side, Y
	sta OAM_ram, Y
	iny
	lda r_left_side, Y
	sta OAM_ram, Y
	iny
	bne @go_forward
		lda #$01
		sta sprite_flicker
		rts
@go_backward_init:
	ldx #$00
	ldy #$fc
;	ldy #$FC
@go_backward:
	lda r_left_side, X
	sta OAM_ram, Y
	iny
	inx
	lda r_left_side, X
	sta OAM_ram, Y
	iny
	inx
	lda r_left_side, X
	sta OAM_ram, Y
	iny
	inx
	lda r_left_side, X
	sta OAM_ram, Y
	tya
	sec
	sbc #$08
	tay
	iny
	inx
	bne @go_backward
		lda #$00
		sta sprite_flicker
@done:
	rts
